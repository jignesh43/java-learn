public class loop {
    public static void main(String []arg){

        //loop in java
        for (int i =0;i<10;i++){
            System.out.println(i);
        }
        // while loop
        int k=0;
        while(k<15){
            System.out.println(k);
            k++;
        }
        //do-while loop
        int t=0;
        do {
            System.out.println(t);
            t++;
        }
        while (t<15);

    }
}
